    public void saberTipoCombustible() {
        txtCochesDiesel.setText(" " + (Monitor.getContDiesel()));
        txtCocheGasolina.setText("" + (Monitor.getContGasolina()));
    }

    public void saberCantidadLitros() {
        txtCantidadLitrosDiesel.setText("" + Monitor.getDepositoDiesel() + " lts");
        txtCantidadLitrosGasolina.setText("" + Monitor.getDepositoGasolina() + " lts");
    }

    public void pintarTiempo() {
        txtTiempo.setText("" + Temporizador.getTiempo() + " seg");
    }

    public void pintarInfoLitros() {
        if (!Monitor.isControlLitros()) {
            txtInfoDiesel.setText("CANTIDAD DE LITROS: " + Monitor.getLitrosGrupoDiesel());
            txtInfoGasolina.setText("CANTIDAD DE LITROS: " + Monitor.getLitrosGrupoGasolina());
        } else {
            txtInfoDiesel.setText("EL DEPOSITO NO TENIA SUFICIENTES LITROS, LLEVA: " + Monitor.getLitrosGrupoDiesel());
            txtInfoGasolina.setText("EL DEPOSITO NO TENIA SUFICIENTES LITROS, LLEVA: " + Monitor.getLitrosGrupoGasolina());
        }

    }

    public void saberGanador() {
        txtGanador.setVisible(true);

        txtGanador.setText("" + Monitor.getNombreGanador());

    }

    public void pintarRepostar() {
        if (Monitor.isTurnoGasolinero()) {
            txtGasolinero.setText("REPOSTANDO");
        } else {
            txtGasolinero.setText("DEPOSITO REPOSTADO");
        }

    }

