    @Override
    public void run() {
        while (!mon.isFinPorGanador() || !mon.isFinTiempo()) {
            saberCantidadLitros();
            pintarTiempo();
            pintarRepostar();

            pintarInfoLitros();
            saberGanador();
            try {
                Thread.sleep(100);
            } catch (InterruptedException ex) {
                Logger.getLogger(VentanaGasolinera.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }

    public void arrancarHiloArrancar() {
        new Thread(this).start();
    }
